  <h2>Getting To Cooperstown</h2>
  <p>
    For those of you who are unfamiliar with where Cooperstown is located or how to get here, we’ve put together some basic information to make your trip a little easier. As strange as it might sound, Cooperstown has not entered the Uber/Lyft era and taxis are few and far between. We’d strongly recommend that regardless of how you arrive, you have your own form of transportation to explore the surrounding area and to get to/from restaurants, lodging, airports.
  </p>
  <h3><hr>By Air<hr></h3>
  <p>
    Cooperstown is located equidistant, approximately 1.5 hr drive, from the Syracuse Hancock Airport (SYR) and the Albany International Airport (ALB). Rental cars are conveniently available at each airport through various national brands.
  </p>
  <h3><hr>From New York City<hr></h3>
  <p>
    The easiest way to get from NYC to Cooperstown is by car or by taking Amtrak to Albany-Rensselaer and then renting a car.
  <p>
    By Car - Depending on traffic, the drive from Manhattan to Cooperstown can take anywhere from 3.5hrs to 4hrs. It’s a pretty easy drive, so put on your favorite audiobook or podcast and enjoy the ride.
  </p>
  <p>
    By Train - There are multiple trains to/from Moynahan Train Station to Albany-Rensselaer each day. The train is about 2.5hrs each way and has a beautiful view along the Hudson River through the Catskill Mountains. Once you arrive in Albany there is an Enterprise car rental at the station. The drive from Albany is about 1hr and 20min. Pro tip: book your tickets early for the lowest costs, and if you’re taking the Amtrak north, be sure to get a seat on the left side of the train to enjoy the best views!
  </p>
  <h3><hr>Village Trolley<hr></h3>
  <p>
    The Village of Cooperstown has a trolley system that runs from 8:30am - 7:30pm each day during the summer. It’s an easy way to get around town without having to find parking and costs $2.50 for an all day pass. For more information on trolley lots and stops throughout town please visit: <a target="_blank" href="https://www.cooperstownny.org/trolley-system/">https://www.cooperstownny.org/trolley-system/</a>
  </p>
